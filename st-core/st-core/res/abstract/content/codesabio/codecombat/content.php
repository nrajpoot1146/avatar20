<div id="event_abstract" style="display: none;">
    <div>
        <h3>The contest consist of 2 rounds:</h3>
        <ul>
            <li>
                <h4>Round 1:</h4>
                <ul>
                    <li>This round consists of <b>10 mcq</b> based questions.</li>
                    <li>The question can be to predict the error or output of program.</li>
                </ul>
                <h4>Duration:</h4>
                <p>Time duration of this round will be of 10 minutes.</p>
            </li>
            <li>
                <h4>Round 2:</h4>
                <ul>
                    <li>This round deals with the pure coding part and logical ability. There will be <b>3 problems</b> 3 problems with level of difficulty may be easy, medium or hard.</li>
                    <li>These problems may be the daily life based problems to be implemented.</li>
                </ul>
                <h4>Duration:</h4>
                <p>Time duration of this round will be of <b>45 minutes</b>.</p>
                <h4>Criteria to choose winner of round 2:</h4>
                <b>Team with maximum number of answers will be the winner.</b>
                <p>In case of tie:</p>
                <ul>
                    <li>Program with more successful test cases will be considered.</li>
                    <li>Program of least time complexity.</li>
                </ul>
            </li>
            <h4>Criteria to choose final winner:</h4>
            <ol>
                <li>Candidate who scores total maximum points in both rounds will be declared as winner.</li>
                <li>Final points will be sum of 1st round points + 2nd rounds points.</li>
            </ol>
            <h4>General rules to follow:</h4>
            <ol>
                <li>Team size may be of maximum 2 candidates.</li>
                <li>Implementation language may be any one of C, C++, Java or python.</li>
                <li>Candidates may bring their own laptops, chargers, extension boards etc.</li>
                <li>Judges decision will be final to decide.</li>
            </ol>
        </ul>
    </div>
