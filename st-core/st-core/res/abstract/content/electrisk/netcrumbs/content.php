            access control and alarm systems. When connected with the Internet, home devices are an important constituent of the Internet of things.</p>
        <p>A home automation system typically connects controlled devices to a central hub or “gateway”. The user interface for control of the system uses either wall-mounted terminals, table or desktop computers, a mobile phone
