        <h3>There are three rounds: </h3>
        <ol>
            <li>In first round there will be a quiz of 25 questions to answer within 30 mins to quaify this round. After qualifying this round you jump to 2nd round.</li>
            <li>In 2nd round will be the group event, a problem statement will be given. According to that problem statement participant would complete the task with the help of software, hardware and Bluetooth module.</li>
            <li>In 3rd round you have to use wifi module to connect all the devices for further data transfer and proper working.</li>
        </ol>
